# 1차원 리스트 생성 (길이 3)
a = [0] * 3
print(a)  # 출력: [0, 0, 0]

# 2차원 리스트 생성 (3x3, 모두 0으로 초기화)
b = [[0] * 3 for _ in range(3)]
print(b)  # 출력: [[0, 0, 0], [0, 0, 0], [0, 0, 0]]

# 2차원 리스트 특정 위치 값 변경
b[0][1] = 2  # 첫 번째 행, 두 번째 열 값을 2로 변경
b[1][1] = 4  # 두 번째 행, 두 번째 열 값을 4로 변경
print(b)  # 출력: [[0, 2, 0], [0, 4, 0], [0, 0, 0]]

# 2차원 리스트 값 출력 (중첩 반복문)
for x in b:
    for y in x:
        print(y, end=' ')  # 각 행의 값들을 한 줄로 출력
    print()  # 각 행 출력 후 줄바꿈